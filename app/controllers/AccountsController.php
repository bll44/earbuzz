	public function generateStreamKey()
	{
		$user = Auth::user();
		$user->streamingKey = new StreamingKey;
		$user->streamingKey->user_id = $user->id;
		$user->streamingKey->save();

		return Redirect::to('account/' . $user->id);

	}

	public function resetStreamKey()
	{
		$key = Auth::user()->streamingKey->resetKey();
		return Response::json(['key' => $key]);
	}

